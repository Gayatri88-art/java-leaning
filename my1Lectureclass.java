public class my1Lectureclass{
  public static void main(String args[]){
    System.out.println("hellow,world");
    System.out.println("hellow world");
    System.out.println("hellow,\nworld");
    System.out.print("name;-,\tgayatri"); 

    //print() → prints, stays same line.
    //println() → prints, then goes to new line.
    //\n → new line inside the text.
    //\t → tab space inside the text.

     // 1️⃣ Print name and age on the same line
     System.out.print("My name is John. ");
     System.out.println("I am 22 years old.");

     // 2️⃣ Print the result of 5 + 3
     System.out.print("The result of 5 + 3 is: ");
     System.out.println(5 + 3);

     // 3️⃣ Print the learning statement using \n for a new line
     System.out.println("I am learning\nJava!");

     // 4️⃣ Bonus Challenge - Creating a formatted table using \t
     System.out.println("\nName\tAge\tCountry");
     System.out.println("Alice\t25\tUSA");
     System.out.println("Bob\t30\tUK");

  }
}